package HW6;



public class Notebook {
    private String brand;
    private String color;
    private int price;
    private String os;
 
    

public Notebook(String brand, String color, int price, String os){

    this.brand = brand;
    this.color = color;
    this.price = price;
    this.os = os;


}

 public String getBrand(){
    return brand;
 }

 public String color(){
    return color;
 }

 public int price(){
    return price;
 }

 public String os(){
    return os;
 }



 
}
    

